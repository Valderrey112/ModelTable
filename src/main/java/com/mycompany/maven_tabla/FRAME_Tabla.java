    public boolean comprobarCampoVacio(JTextField campo){
        return campo.getText().equals("");
    }
    
    public void vaciarCampos(){
        txtFieldNombre.setText("");
        txtFieldApellido1.setText("");
        txtFieldApellido2.setText("");
        txtFieldEdad.setText("");
    }
    
    //-3 = no inicializado
    //-2 = campo edad no es numero
    //-1 = campo sin completar
    // 0 = campos correctos
    public int comprobarCampos(){
    
        String mensaje = "";
        int valor = -3;
        
        if(comprobarCampoVacio(txtFieldNombre) | 
        comprobarCampoVacio(txtFieldApellido1) | 
        comprobarCampoVacio(txtFieldApellido2) | 
        comprobarCampoVacio(txtFieldEdad)){
            mensaje = "Algún campo esta sin completar.\nEs necesario rellenar todos los campos.";
            System.out.println(mensaje);
            valor = -1;
        }else if(!comprobarCampoVacio(txtFieldNombre) & 
        !comprobarCampoVacio(txtFieldApellido1) & 
        !comprobarCampoVacio(txtFieldApellido2) & 
        !comprobarCampoVacio(txtFieldEdad)){
            try{
                valor = 0;
            }catch(NumberFormatException e){
                mensaje = "La edad debe ser un número.";
                System.out.println(mensaje);
                valor = -2;
            }
        }
        
        return valor;
        
    }
    
    public void introducirPersona(){
    
        if(comprobarCampos()==0){
        
            int num = Integer.valueOf(txtFieldEdad.getText());
                String nombre = txtFieldNombre.getText();
                String apellido1 = txtFieldApellido1.getText();
                String apellido2 = txtFieldApellido2.getText();
            
            if(num > 0){
                Persona nuevaPersona = new Persona(nombre, apellido1, apellido2, num);
                manejador.addPerson(nuevaPersona, rutaTabla);
                if(vaciarCampos)
                    vaciarCampos();
                jTable1.setModel(new TableModel(rutaTabla));
            }else{
                System.out.println("La edad debe de ser mayor a 0");
            }
        
        }
        
    }
    
    public void eliminarPersona(){
    
        if(comprobarCampos()==0){
        
            int num = Integer.valueOf(txtFieldEdad.getText());
            String nombre = txtFieldNombre.getText();
            String apellido1 = txtFieldApellido1.getText();
            String apellido2 = txtFieldApellido2.getText();
            
            if(vaciarCampos)
                    vaciarCampos();

            Persona nuevaPersona = new Persona(nombre, apellido1, apellido2, num);
            manejador.deletePerson(nuevaPersona, rutaTabla);

            jTable1.setModel(new TableModel(rutaTabla));
        
        }
        
    }
    
