    public void botonPersona(int valor){
    
        switch(valor) {
            case 0:
              introducirPersona();
              break;
            case 1:
              eliminarPersona();
              break;
            case 2:
              btnPersona.setText("FILTRAR");
              break;
            default:
        }
        
    }
    
